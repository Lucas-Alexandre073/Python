#Tipos de números: int e float
#int: Número inteiro
#O tipo int representa qualquer número 
#Positivo ou negativo
#Números int sem sinal são considerados positivos
print(11)#int
print(-11)#int
print(0)#int
#float:Número com ponto flutuante
#O tipo float representa qualquer número 
#Sendo ele positivo ou negativo com ponto flutuante
#float sem sinal é considerado positivo
print(1.1, 10.11)#float
print(0.0, -1.5)#float
#A função type mostra o tipo que o python inferiu ao valor 
print(type('Lucas'))#string
print(type(0))#int
print(type(1.7))#float
